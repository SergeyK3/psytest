def analyze_disc_discrepancy():
    """–ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º –Ω–µ—Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–µ –≤ DISC —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞—Ö"""
    print("üîç –ê–ù–ê–õ–ò–ó –ù–ï–°–û–û–¢–í–ï–¢–°–¢–í–ò–Ø DISC –†–ï–ó–£–õ–¨–¢–ê–¢–û–í")
    print("=" * 50)
    
    # –í–∞—à–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã
    your_results = {"D": 32, "I": 20, "S": 28, "C": 24}
    total = sum(your_results.values())
    
    # –¢–µ–∫—É—â–∞—è —Å–∏—Å—Ç–µ–º–∞: 8 –≤–æ–ø—Ä–æ—Å–æ–≤, –ø–æ 2 –Ω–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏—é
    current_system = {
        "questions_total": 8,
        "questions_per_category": 2,
        "scale": "1-5",
        "expected_min_per_category": 2,  # 2 –≤–æ–ø—Ä–æ—Å–∞ √ó 1 –±–∞–ª–ª
        "expected_max_per_category": 10  # 2 –≤–æ–ø—Ä–æ—Å–∞ √ó 5 –±–∞–ª–ª–æ–≤
    }
    
    print("üìä –í–∞—à–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã:")
    print(f"   D = {your_results['D']} –±–∞–ª–ª–æ–≤")
    print(f"   I = {your_results['I']} –±–∞–ª–ª–æ–≤")  
    print(f"   S = {your_results['S']} –±–∞–ª–ª–æ–≤")
    print(f"   C = {your_results['C']} –±–∞–ª–ª–æ–≤")
    print(f"   –í—Å–µ–≥–æ: {total} –±–∞–ª–ª–æ–≤")
    
    print("\nüìã –¢–µ–∫—É—â–∞—è —Å–∏—Å—Ç–µ–º–∞:")
    print(f"   ‚Ä¢ {current_system['questions_total']} –≤–æ–ø—Ä–æ—Å–æ–≤ –≤—Å–µ–≥–æ")
    print(f"   ‚Ä¢ {current_system['questions_per_category']} –≤–æ–ø—Ä–æ—Å–∞ –Ω–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏—é")
    print(f"   ‚Ä¢ –®–∫–∞–ª–∞: {current_system['scale']} –±–∞–ª–ª–æ–≤")
    print(f"   ‚Ä¢ –û–∂–∏–¥–∞–µ–º—ã–π –¥–∏–∞–ø–∞–∑–æ–Ω: {current_system['expected_min_per_category']}-{current_system['expected_max_per_category']} –±–∞–ª–ª–æ–≤ –Ω–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏—é")
    
    print("\n‚ùó –ü–†–û–ë–õ–ï–ú–ê:")
    print(f"   –í–∞—à–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –ø—Ä–µ–≤—ã—à–∞—é—Ç –º–∞–∫—Å–∏–º—É–º!")
    for category, score in your_results.items():
        if score > current_system['expected_max_per_category']:
            excess = score - current_system['expected_max_per_category']
            print(f"   ‚Ä¢ {category}: {score} > {current_system['expected_max_per_category']} (–ø—Ä–µ–≤—ã—à–µ–Ω–∏–µ –Ω–∞ {excess})")
    
    print("\nü§î –í–û–ó–ú–û–ñ–ù–´–ï –ü–†–ò–ß–ò–ù–´:")
    
    # –ì–∏–ø–æ—Ç–µ–∑–∞ 1: –ë–æ–ª—å—à–µ –≤–æ–ø—Ä–æ—Å–æ–≤ –Ω–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏—é
    estimated_questions_per_cat = max(your_results.values()) / 5  # –ï—Å–ª–∏ –≤—Å–µ –æ—Ç–≤–µ—Ç—ã –±—ã–ª–∏ 5
    print(f"\n1. –ë–æ–ª—å—à–µ –≤–æ–ø—Ä–æ—Å–æ–≤ –Ω–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏—é:")
    print(f"   ‚Ä¢ –ï—Å–ª–∏ –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–µ –æ—Ç–≤–µ—Ç—ã (–≤—Å–µ –ø–æ 5): {estimated_questions_per_cat:.1f} –≤–æ–ø—Ä–æ—Å–æ–≤ –Ω–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏—é")
    print(f"   ‚Ä¢ D=32: –Ω—É–∂–Ω–æ {32/5:.1f} = 6-7 –≤–æ–ø—Ä–æ—Å–æ–≤ —Å –æ—Ç–≤–µ—Ç–æ–º 5")
    
    # –ì–∏–ø–æ—Ç–µ–∑–∞ 2: –î—Ä—É–≥–∞—è —Å–∏—Å—Ç–µ–º–∞ –ø–æ–¥—Å—á–µ—Ç–∞
    print(f"\n2. –°—Ç–∞—Ä–∞—è —Å–∏—Å—Ç–µ–º–∞ –ø–æ–¥—Å—á–µ—Ç–∞:")
    print(f"   ‚Ä¢ –í–æ–∑–º–æ–∂–Ω–æ, –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–ª–∞—Å—å –Ω–∞–∫–æ–ø–∏—Ç–µ–ª—å–Ω–∞—è —Å–∏—Å—Ç–µ–º–∞")
    print(f"   ‚Ä¢ –ò–ª–∏ –∫–∞–∂–¥—ã–π –æ—Ç–≤–µ—Ç –≤–ª–∏—è–ª –Ω–∞ –Ω–µ—Å–∫–æ–ª—å–∫–æ –∫–∞—Ç–µ–≥–æ—Ä–∏–π")
    
    # –ì–∏–ø–æ—Ç–µ–∑–∞ 3: –î–∞–Ω–Ω—ã–µ –∏–∑ –¥—Ä—É–≥–æ–≥–æ —Ç–µ—Å—Ç–∞
    if total > 40:
        alternative_questions = total / 20  # –ï—Å–ª–∏ —Å—Ä–µ–¥–Ω–∏–π –æ—Ç–≤–µ—Ç 2.5
        print(f"\n3. –ê–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω–∞—è –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—è:")
        print(f"   ‚Ä¢ –ü—Ä–∏ —Å—Ä–µ–¥–Ω–µ–º –æ—Ç–≤–µ—Ç–µ 2.5: {alternative_questions:.0f} –≤–æ–ø—Ä–æ—Å–æ–≤ –Ω–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏—é")
        print(f"   ‚Ä¢ –í–æ–∑–º–æ–∂–Ω–æ, —ç—Ç–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –∏–∑ –¥—Ä—É–≥–æ–π –≤–µ—Ä—Å–∏–∏ —Ç–µ—Å—Ç–∞")

