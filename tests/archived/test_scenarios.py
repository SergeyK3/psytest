# Функция для конвертации HEXACO оценок в правильный формат
def convert_hexaco_scores(scores):
    """Конвертирует сырые баллы HEXACO в формат с именами факторов"""
    mapping = {
        "H": "Честность-Скромность",
        "E": "Эмоциональность", 
        "X": "Экстраверсия",
        "A": "Доброжелательность",
        "C": "Добросовестность",
        "O": "Открытость опыту"
    }
    return {mapping[k]: v for k, v in scores.items()}

# Функция для конвертации DISC оценок в полные названия
def convert_disc_scores(scores):
    """Конвертирует сырые баллы DISC в формат с полными названиями"""
    mapping = {
        "D": "Доминирование",
        "I": "Влияние", 
        "S": "Устойчивость",
        "C": "Подчинение правилам"
    }
    return {mapping[k]: v for k, v in scores.items()}